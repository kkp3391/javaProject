package znznnznnn;

public class Student {
	private int stdNo;
	private String name;
	private int score;

    


    
   public Student(int stdNo, String name, int score) {
      super();
	   this.stdNo = stdNo;
        this.name = name;
        this.score = score;

    
   }
    

    public int getStdNo() {
	return stdNo;
}


public void setStdNo(int stdNo) {
	this.stdNo = stdNo;
}


public void setName(String name) {
	this.name = name;
}


public String getName() {
	return name;
}


public int getScore() {
	return score;
}


public void setScore(int score) {
	this.score = score;
}


}	
   


